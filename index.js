let fruit = "バナナ";

switch (fruit) {
    case "バナナ":
        console.log("バナナの値段は130円です!");
        break;
    case "メロン":
        console.log("メロンの値段は200円です!");
        break;
    case "いちご":
        console.log("いちごの値段は160円です!");
        break;
    default:
        console.log("正しいフルーツを指定してください");
        break;
}
